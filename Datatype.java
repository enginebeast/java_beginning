package hello;

public class Datatype {

	public static void main(String[] args) {
		char a = 'A';
		int b = 100;
		long c = 100000l;
		float d = 1.23456789f;
		
		System.out.println("a = " + a);
		System.out.println("int a = " + (int)a);
		System.out.println("b = " + b);
		System.out.println("c = " + c);
		System.out.println("d = " + d);
	}

}
